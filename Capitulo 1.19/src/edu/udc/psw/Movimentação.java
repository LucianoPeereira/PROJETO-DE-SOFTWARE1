package edu.udc.psw;

public class Movimentação {
	private String Descrição;
	private Double Valor;
	private String MovimentaçãoCOuD;
	
	
	public String getDescrição() {
		return Descrição;
	}
	public void setDescrição(String descrição) {
		Descrição = descrição;
	}
	public Double getValor() {
		return Valor;
	}
	public void setValor(Double valor) {
		Valor = valor;
	}
	public String getMovimentaçãoCOuD() {
		return MovimentaçãoCOuD;
	}
	public void setMovimentaçãoCOuD(String movimentaçãoCOuD) {
		MovimentaçãoCOuD = movimentaçãoCOuD;
	}
	
	
}
